        // Fonction pour obtenir une question aléatoire sans répétition
        let usedQuestionsIndices = [];
        
        function getRandomQuestion() {
            // Si toutes les questions ont été utilisées, réinitialiser
            if (usedQuestionsIndices.length === questions.length) {
                usedQuestionsIndices = [];
            }
            
            let randomIndex;
            do {
                randomIndex = Math.floor(Math.random() * questions.length);
            } while (usedQuestionsIndices.includes(randomIndex));
            
            usedQuestionsIndices.push(randomIndex);
            return questions[randomIndex];
        }

        let currentQuestion = 0;
        let score = 0;
        let gameActive = true;
        let questionsList = [];
        let playerPseudo = '';
        let health = 20;
        const maxHealth = 20;

        // Fonction pour mettre à jour le joint qui se consomme
        function updateHealthBar() {
            const percentage = (health / maxHealth) * 100;
            const circumference = 314; // 2 * Math.PI * 50 (rayon = 50)
            const strokeDashoffset = circumference - (percentage / 100) * circumference;
            
            const circleFill = document.getElementById('health-circle-fill');
            const textCircle = document.getElementById('health-text-circle');
            
            circleFill.style.strokeDashoffset = strokeDashoffset;
            document.getElementById('health-text-circle').textContent = health + '/' + maxHealth;
            
            // Changer les couleurs selon la santé
            circleFill.classList.remove('critical', 'warning', 'good');
            textCircle.classList.remove('critical', 'warning', 'good');
            
            if (health > 14) {
                // Vert - Excellent
                circleFill.classList.add('good');
                textCircle.classList.add('good');
                circleFill.style.filter = 'drop-shadow(0 0 8px rgba(16, 185, 129, 0.4))';
            } else if (health > 10) {
                // Bleu - Bon
                circleFill.style.filter = 'drop-shadow(0 0 8px rgba(59, 130, 246, 0.4))';
            } else if (health > 6) {
                // Orange - Moyen
                circleFill.classList.add('warning');
                textCircle.classList.add('warning');
                circleFill.style.filter = 'drop-shadow(0 0 8px rgba(249, 115, 22, 0.6))';
            } else {
                // Rouge - Critique
                circleFill.classList.add('critical');
                textCircle.classList.add('critical');
                circleFill.style.filter = 'drop-shadow(0 0 12px rgba(239, 68, 68, 0.8))';
            }
        }

        // Initialiser le pseudo basé sur le système de compte
        function initializePlayer() {
            if (authManager && authManager.isLoggedIn()) {
                playerPseudo = authManager.getCurrentUser();
            } else {
                playerPseudo = localStorage.getItem('playerPseudo') || 'Anonyme';
            }
            
            // Initialiser la barre de vie
            updateHealthBar();
        }

        function renderQuestion() {
            if (!gameActive) return;
            
            const quiz = document.getElementById('quiz');
            // Obtenir une question aléatoire
            const q = getRandomQuestion();
            questionsList.push(q);
            
            let html = `
                <div class="question active">
                    <div class="question-title">${q.question}</div>
                    <div class="options">
            `;
            
            q.options.forEach((option, index) => {
                html += `<div class="option" onclick="selectOption(${index})">${option}</div>`;
            });
            
            html += `
                    </div>
                    <div class="answer">
                        <div class="answer-title">Réponse:</div>
                        <div class="answer-text">${q.answer}</div>
                    </div>
                    <div class="buttons">
                        <button class="btn-submit" onclick="submitAnswer()">Valider</button>
                    </div>
                </div>
            `;
            
            quiz.innerHTML = html;
        }

        let selectedOption = null;

        function selectOption(index) {
            selectedOption = index;
            document.querySelectorAll('.option').forEach((el, i) => {
                el.classList.remove('selected');
                if (i === index) el.classList.add('selected');
            });
        }

        function submitAnswer() {
            if (selectedOption === null) return;
            
            const q = questionsList[currentQuestion];
            const options = document.querySelectorAll('.option');
            
            options.forEach((el, i) => {
                if (i === q.correct) {
                    el.classList.add('correct');
                } else if (i === selectedOption && i !== q.correct) {
                    el.classList.add('incorrect');
                }
                el.style.pointerEvents = 'none';
            });
            
            if (selectedOption === q.correct) {
                score++;
            } else {
                // Mauvaise réponse - retirer 1 point de vie
                health--;
                updateHealthBar();
                
                // Si la vie atteint 0, game over
                if (health <= 0) {
                    health = 0;
                    updateHealthBar();
                    gameActive = false;
                    
                    document.querySelector('.answer').classList.add('show');
                    document.querySelector('.btn-submit').style.display = 'none';
                    
                    const gameOverBtn = document.createElement('button');
                    gameOverBtn.className = 'btn-gameover';
                    gameOverBtn.textContent = '💀 GAME OVER - Fin du jeu';
                    gameOverBtn.onclick = () => {
                        showResults();
                    };
                    document.querySelector('.buttons').innerHTML = '';
                    document.querySelector('.buttons').appendChild(gameOverBtn);
                    return;
                }
            }
            
            document.querySelector('.answer').classList.add('show');
            document.querySelector('.btn-submit').style.display = 'none';
            
            const nextBtn = document.createElement('button');
            nextBtn.className = 'btn-next';
            nextBtn.textContent = 'Suivant';
            nextBtn.onclick = nextQuestion;
            document.querySelector('.buttons').appendChild(nextBtn);
        }

        function nextQuestion() {
            const container = document.querySelector('.container');
            
            // Animation de sortie à droite
            container.classList.remove('slide-in');
            container.classList.add('slide-out');
            
            // Attendre la fin de l'animation avant de changer la question
            setTimeout(() => {
                currentQuestion++;
                if (gameActive) {
                    selectedOption = null;
                    document.getElementById('current').textContent = currentQuestion + 1;
                    renderQuestion();
                    
                    // Animation d'entrée depuis la gauche
                    container.classList.remove('slide-out');
                    container.classList.add('slide-in');
                }
            }, 500);
        }

        function showResults() {
            document.getElementById('quiz').style.display = 'none';
            document.getElementById('end-screen').classList.add('show');
            document.getElementById('final-score').textContent = score;
            document.getElementById('final-questions').textContent = currentQuestion;
            
            // Masquer/afficher le message selon si connecté ou pas
            const noAccountMsg = document.getElementById('no-account-message');
            if (authManager && authManager.isLoggedIn()) {
                noAccountMsg.style.display = 'none';
            } else {
                noAccountMsg.style.display = 'block';
            }
            
            // Sauvegarder le score
            saveScore(playerPseudo, score, currentQuestion);
        }
        
        function saveScore(pseudo, score, questionsAnswered) {
            // Sauvegarder UNIQUEMENT pour les utilisateurs connectés
            if (authManager && authManager.isLoggedIn()) {
                authManager.addScore(score, questionsAnswered, 'Quiz').then(success => {
                    if (success) {
                        console.log('Score sauvegardé dans le compte utilisateur!');
                    } else {
                        console.error('Erreur lors de la sauvegarde du score');
                    }
                });
            } else {
                // Les joueurs sans compte ne peuvent PAS sauvegarder
                console.log('⚠️ Score non sauvegardé : veuillez créer un compte pour sauvegarder vos scores.');
            }
        }

        function gameOver() {
            gameActive = false;
            showResults();
        }

        document.getElementById('total').textContent = "∞";
        initializePlayer();
        renderQuestion();
